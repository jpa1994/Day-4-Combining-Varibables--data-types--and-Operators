// Problem 3: Type Coercion Exploration

let num = 5;
let str = 10;

console.log("Num:", num);
console.log("Str:", str);

// Operations

num += str;
console.log("Num += Str:", num);
str -= num;
console.log("Str -= Num:", str);

num == str;
console.log("Equals to:", num == str);
num === str;
console.log("Strictly Equals to:", num === str);
